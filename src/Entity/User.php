<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;
use Symfony\Component\Security\Core\User\UserInterface;
use Symfony\Bridge\Doctrine\Validator\Constraints\UniqueEntity;

/**
 * @ORM\Entity(repositoryClass="App\Repository\UserRepository")
 * @UniqueEntity(
 * fields={"email"}, 
 * message="Cette adresse email existe déjà! avez vous oubliez votre mot de passe?"
 * )
 */
class User implements UserInterface
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     * @Assert\Email(
     *     message = "The email '{{ value }}' is not a valid email.",
     *     checkMX = true
     * )
     */
    private $email;

    /**
     * @ORM\Column(type="string", length=255)     * @Assert\Length(
     *      min = 4,
     *      minMessage = "Votre pseudonyme est trop court, veuillez en choisir un supérieur à {{ limit }} characteres"
     * )
     */
    private $username;

    /**
     * @ORM\Column(type="string", length=255)
     * @Assert\Length(
     *      min = 8,
     *      minMessage = "Le mot de passe doit avoir au moins {{ limit }} characteres"
     * )
     * @Assert\EqualTo(
     *     propertyPath = "confirm_password",
     *     message = "Le mot de passe ne correspond pas à sa confirmation"
     * )
     */
    private $password;

    /**
     * @Assert\EqualTo(
     *     propertyPath = "confirm_password",
     *     message = "Le mot de passe de confirmation ne correspond pas à celui précédemment fourni"
     * )
     */
    public $confirm_password;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getEmail(): ?string
    {
        return $this->email;
    }

    public function setEmail(string $email): self
    {
        $this->email = $email;

        return $this;
    }

    public function getUsername(): ?string
    {
        return $this->username;
    }

    public function setUsername(string $username): self
    {
        $this->username = $username;

        return $this;
    }

    public function getPassword(): ?string
    {
        return $this->password;
    }

    public function setPassword(string $password): self
    {
        $this->password = $password;

        return $this;
    }

    // Need to be implemented by UserInterface

    public function getSalt()
    {
        // The bcrypt and argon2i algorithms don't require a separate salt.
        // You *may* need a real salt if you choose a different encoder.
        return null;
    }

    public function getRoles()
    {
        return array('ROLE_USER');
        return $this->roles;
    }

    public function eraseCredentials()
    {
    }
}
